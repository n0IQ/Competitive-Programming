void solve()
{
	string s;
	cin >> s;

	stack<char> stk;
	int frst = 1, scnd = 0;

	for (char c : s) {
		if (stk.empty() || stk.top() != c) {
			stk.push(c);
		}
		else {
			stk.pop();
			frst = 1 - frst; scnd = 1 - scnd;
		}
	}

	if (frst) {
		cout << "No" << '\n';
	}
	else {
		cout << "Yes" << '\n';
	}
}

