void solve()
{
	ll n, t1, t2, t3;
	cin >> n;

	if (n <= 6) {
		cout << 15 << '\n';
		return;
	}

	ll pizzas = n / 6;

	t1 = t2 = t3 = pizzas * 15;
	if (pizzas * 6 < n) t1 += 15;

	if ((n % 6) + 6 <= 8) t2 += 5;
	else t2 += 20;

	if ((n % 6) + 6 <= 10) t3 += 10;
	else t3 += 25;

	cout << min({t1, t2, t3}) << '\n';
}

